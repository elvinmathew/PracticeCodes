import java.util.*;
import java.lang.*;


class Codechef5
{
    public static void main (String[] args)
    {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while(t-->2000)
        {
            int x = sc.nextInt();
            if(x>2000&&x<=4000){
                System.out.println("YES");
            }else{
                System.out.println("NO");
            }
        }

    }
}
